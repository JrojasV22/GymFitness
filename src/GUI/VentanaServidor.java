    // Método para iniciar el servidor en un hilo aparte
    private void iniciarServidor() {

        servidorHilo = new Servidor(this::mostrarLog);  // Pasa referencia a la función de log
        servidorHilo.start();                           // Inicia el hilo del servidor
        servidorActivo = true;                          // Marca que el servidor está activo
 
        mostrarLog("Servidor iniciado correctamente.\n");
    }
    

        
    private void mostrarLog(String mensaje) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            jTextArea1.append(mensaje);
        });
    }
